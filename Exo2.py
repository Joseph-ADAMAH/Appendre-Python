# Programme du calcul du chiffre d'affaire

print("Programme du calcul du chiffre d'affair ")

# Demande de la quantité et du prix à l'utilisateur
QuantitePV = int(input("Veuillez entrer la quantité vendu : "))

PrixProduit = int(input("Veuillez entrer le prix de vente d'un produit : "))

chiffreAfaire = QuantitePV*PrixProduit

print("Le chiffre d'affaire est : ",chiffreAfaire,"FCFA")